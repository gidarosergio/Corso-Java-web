public class Giocattolo {
        private int id;
        private String nome;
        private double prezzo;
        private String etaConsigliata;

        public Giocattolo(int id, String nome, double prezzo, String etaConsigliata) {
            this.id = id;
            this.nome = nome;
            this.prezzo = prezzo;
            this.etaConsigliata = etaConsigliata;
        }
    }